# **Bài 12: Tìm số Nhỏ nhất trong Danh sách**

# - **Mô tả bài toán:** Cho `temperatures = [25, 18, 30, 22]`. Tìm và in số nhỏ nhất.
# - **Input:** `temperatures`
# - **Output:** Số nhỏ nhất.
# - **Ví dụ kiểm thử:** `18`
# - **Gợi ý:** Sử dụng hàm `min()`.
temperatures = [25, 18, 30, 22]
print(min(temperatures))