# **Bài 13: Sắp xếp Danh sách Tăng dần**

# - **Mô tả bài toán:** Cho `data = [5, 2, 8, 1]`. Sắp xếp tăng dần. In danh sách đã sắp xếp.
# - **Input:** `data`
# - **Output:** Danh sách đã sắp xếp.
# - **Ví dụ kiểm thử:** `[1, 2, 5, 8]`
# - **Gợi ý:** Sử dụng phương thức `.sort()`.
data = [5, 2, 8, 1]
data.sort()
print(data)